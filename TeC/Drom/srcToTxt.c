#include <stdio.h>
#include <stdlib.h>

// 26bit のデコードされた値
char *a[] = {
  "00000000000000000000000001",  // 00
  "00000000000000000000000010",  // 01
  "00000000000000000000000100",  // 02
  "00000000000000000000001000",  // 03
  "00000000000000000000010000",  // 04
  "00000000000000000000100000",  // 05
  "00000000000000000001000000",  // 06
  "00000000000000000010000000",  // 07
  "00000000000000000100000000",  // 08
  "00000000000000001000000000",  // 09
  "00000000000000010000000000",  // 10
  "00000000000000100000000000",  // 11
  "00000000000001000000000000",  // 12
  "00000000000010000000000000",  // 13
  "00000000000100000000000000",  // 14
  "00000000001000000000000000",  // 15
  "00000000010000000000000000",  // 16
  "00000000100000000000000000",  // 17
  "00000001000000000000000000",  // 18
  "00000010000000000000000000",  // 19
  "00000100000000000000000000",  // 20
  "00001000000000000000000000",  // 21
  "00010000000000000000000000",  // 22
  "00100000000000000000000000",  // 23
  "01000000000000000000000000",  // 24
  "10000000000000000000000000"   // 25
};

#define LN 1000
int main() {
  char buf[LN];
  while (fgets(buf, LN, stdin)!=0) {
    if (buf[0]=='#') {
      continue;
    } else {
      int v;
      sscanf(buf, "%d", &v);
      if (v<0 || sizeof(a)/sizeof(char *)<=v) exit(1);
      printf("%s\n", a[v]);
    }
  }
  return 0;
}
